# Demonstrates common functions used with numbers.
import math

# Absolute value.
print(abs(-123), '\n')

# Powers.
x = 5**2
print(x)
print(pow(5, 2))
print(math.sqrt(25), '\n')

# Larger/smaller number.
print(max(5, 2))
print(min(5, 2), '\n')

# Rounding.
print(round(1.5))
print(math.floor(1.7))
print(math.ceil(1.2), '\n')
